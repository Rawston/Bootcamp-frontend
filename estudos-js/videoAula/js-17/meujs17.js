/* Igualdade e Igualdade Rigorosa (strict equality) */

//Igualdade ==> Verrifica valor

console.log("20" == 20)
console.log("0" == false)
console.log(0 == false)
console.log("1" == true)
console.log(1 == true)
console.log(null == undefined)
console.log("Ex. Igualdade ==> Verrifica valor")

// Igualdade Rigorosa ==> Verrifica valor & TIPO.

console.log("20" === 20)
console.log("0" === false)
console.log(0 === false)
console.log("1" === true)
console.log(1 === true)
console.log(null === undefined)
console.log("Ex. Igualdade Rigorosa ==> Verrifica valor & TIPO.")